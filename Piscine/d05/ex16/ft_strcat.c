char	*ft_strcat(char *dest, char *src)
{
	int i;
	int c;

	c = 0;
	i = 0;
	while (dest[i])
		i++;
	while (src[c])
	{
		dest[i] = src[c];
		c++;
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
